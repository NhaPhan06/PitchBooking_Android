package com.example.pitchbooking.activity.Model;

public class Pitch {
    public int Id;
    public String Name;

    public Pitch(int id, String name) {
        Id = id;
        Name = name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
